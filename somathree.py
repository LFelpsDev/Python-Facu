def somathree():
    a = int(input('Digite o valor de a: '))
    b = int(input('Digite o valor de b: '))
    c = int(input('Digite o valor de C: '))
    qtde = 2
    if a % qtde == 0:
      print(f'O Numero {a} É divisel por 2')
    if b % qtde == 0:
      print(f'O Numero {b} É divisel por 2')
    if c % qtde == 0:
      print(f'O Numero {c} É divisel por 2')

somathree()